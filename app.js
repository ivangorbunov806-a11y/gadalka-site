function buildResults() {
  return Object.fromEntries(
    THEMES.map((theme) => {
      const profile = THEME_PROFILES[theme];
      const themeResults = Object.fromEntries(
        CARDS.map((card) => {
          const omen = CARD_OMENS[card];
          const message = [profile.opening, omen.sign, omen.energy, profile.advice, omen.advice, profile.closing].join(" ");
          return [card, message];
        }),
      );

      return [theme, themeResults];
    }),
  );
}

